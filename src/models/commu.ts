import mongoose from 'mongoose';

const CommuSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  author: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const Commu = mongoose.models.Commu || mongoose.model('Commu', CommuSchema);
export default Commu;